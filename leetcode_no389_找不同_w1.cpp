using namespace std;

struct ListNode {
    int val;
    ListNode* next;
    ListNode(int x) : val(x), next(NULL) {}

};

struct TreeNode {
    int val;
    TreeNode* left;
    TreeNode* right;
    TreeNode(int x) : val(x), left(NULL), right(NULL) {}
};

char findTheDifference(string s, string t) {
    int res = 0;
    for (char c : t) {
        res += c;
    }
    for (char c : s) {
        res -= c;
    }
    return res;
}